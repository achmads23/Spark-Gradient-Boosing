    public SelectedImage(File file) throws IOException {
        initComponents();
        
        String filepath = file.getAbsolutePath();
        jTextField1.setText(filepath);
        jTextField1.setEditable(false);
        BufferedImage img = ImageIO.read(new File(filepath));
        int lebar = img.getWidth();
        jTextField2.setText(String.valueOf(lebar));
        int Panjang = img.getHeight();
        jTextField3.setText(String.valueOf(Panjang));
        Image dimg;
        if(lebar < Panjang){
            int ratio = 55000 / lebar;
            int rpanjang= Panjang * ratio/100;
            dimg = img.getScaledInstance(550,rpanjang,Image.SCALE_SMOOTH);
        } else if(lebar > Panjang) {
            int ratio = 55000 / Panjang;
            int rlebar= lebar * ratio/100;
            dimg = img.getScaledInstance(rlebar,550,Image.SCALE_SMOOTH);
        } else {
            dimg = img.getScaledInstance(550,550,Image.SCALE_SMOOTH);
        }
        ImageIcon icon = new ImageIcon(dimg);
        jLabel1.setIcon(icon);
        jLabel1.setHorizontalAlignment(JLabel.CENTER);
        jLabel1.repaint();
        setLocationRelativeTo(null);
        pack();
    }

